package com.example.app2_recyclerview.Models;

public class Movie {
    String title, genre, release_date;

    public Movie(String title, String genre, String release_date) {
        this.title = title;
        this.genre = genre;
        this.release_date = release_date;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getGenre() {
        return genre;
    }

    public void setGenre(String genre) {
        this.genre = genre;
    }

    public String getRelease_date() {
        return release_date;
    }

    public void setRelease_date(String release_date) {
        this.release_date = release_date;
    }
}
